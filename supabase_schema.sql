-- 3. Table: asistencia
CREATE TABLE IF NOT EXISTS public.asistencia (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    fecha TEXT NOT NULL, -- Format: dd/MM/yyyy
    dni TEXT NOT NULL,
    nombre TEXT NOT NULL,
    supervisor TEXT NOT NULL,
    estado TEXT NOT NULL CHECK (estado IN ('Trabajo', 'Descanso', 'Falta')),
    codigo_labor TEXT,
    lote TEXT,
    hora_inicio TEXT, -- Format: HH:mm
    hora_fin TEXT,    -- Format: HH:mm
    created_at TIMESTAMPTZ DEFAULT NOW(),
    CONSTRAINT fk_operarios FOREIGN KEY (dni) REFERENCES public.operarios(dni) ON DELETE CASCADE
);

-- Indexes for fast queries
CREATE INDEX IF NOT EXISTS idx_asistencia_fecha ON public.asistencia (fecha);
CREATE INDEX IF NOT EXISTS idx_asistencia_supervisor_fecha ON public.asistencia (supervisor, fecha);
CREATE INDEX IF NOT EXISTS idx_asistencia_dni_fecha ON public.asistencia (dni, fecha);

-- 4. Table: historial
CREATE TABLE IF NOT EXISTS public.historial (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    dni TEXT NOT NULL,
    nombre TEXT NOT NULL,
    supervisor TEXT NOT NULL,
    primera_vez TIMESTAMPTZ DEFAULT NOW(),
    ultima_vez TIMESTAMPTZ DEFAULT NOW(),
    CONSTRAINT uq_dni_supervisor UNIQUE (dni, supervisor)
);

