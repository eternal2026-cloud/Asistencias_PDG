-- 1. Table: operarios
CREATE TABLE IF NOT EXISTS public.operarios (
    dni TEXT PRIMARY KEY,
    nombre_completo TEXT NOT NULL,
    fecha_registro TIMESTAMPTZ DEFAULT NOW(),
    CONSTRAINT dni_length CHECK (char_length(dni) = 8)
);

-- Index on operarios name for quick autocomplete searches
CREATE INDEX IF NOT EXISTS idx_operarios_nombre ON public.operarios (nombre_completo);

-- 2. Table: supervisores
CREATE TABLE IF NOT EXISTS public.supervisores (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre TEXT UNIQUE NOT NULL,
    clave TEXT NOT NULL,
    activo BOOLEAN DEFAULT TRUE,
    fecha_registro TIMESTAMPTZ DEFAULT NOW()
);

